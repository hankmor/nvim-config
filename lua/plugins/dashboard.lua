local logs = { helloworld, neovim, nst1, nst2, nst3, nst4, nst5, nst6, earth, saturn, cacodemon, nami, luffy }

local function randomLogo()
  math.randomseed(os.time())
  local idx = math.random(#logs)
  return string.rep("\n", 1) .. logs[idx] .. "\n"
end

return {
  {
    "nvimdev/dashboard-nvim",
    lazy = false, -- As https://github.com/nvimdev/dashboard-nvim/pull/450, dashboard-nvim shouldn't be lazy-loaded to properly handle stdin.
    opts = {
      config = {
        header = vim.split(randomLogo() .. title, "\n"),
      },
    },
  },
}
